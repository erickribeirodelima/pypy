# Tipo bool (boolean)
# Questionar algo em um programa
# Só existem dois (True) ou (False)
# Existem alguns operadores para questionar, tipo == para questionar se um valor é igual ao outro

numero1 = 12
numero2 = 12
numero3 = 10

print(numero1 == numero2) # True
print(numero1 == numero3) # False
print(numero1 > numero3) # True
print(numero3 < numero2) # True